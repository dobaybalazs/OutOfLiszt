class DefaultList {
  constructor(
    id,
    name,
    products,
    users,
    priority,
    dateMonth,
    dateDay,
    itemCount
  ) {
    this.id = id;
    this.name = name;
    this.products = products;
    this.users = users;
    this.priority = priority;
    this.dateMonth = dateMonth;
    this.dateDay = dateDay;
    this.itemCount = itemCount;
  }
}

export default DefaultList;
